function catalogo_x_clases(string $clase) : array {
    $resultado = [];
    $catalogo = catalogo_completo();

    foreach ($catalogo as $p) {
        if($p["clase"] == $clase){
            //agregar elementos al array
             $resultado[] = $p;
        }
    }

    return $resultado;

}

/* Devuelve los datos de un producto en particular
$idProducto el id unico del producto a mostrar
*/

function producto_x_id(int $idProducto){
 $catalogo = catalogo_completo();

 foreach ($catalogo as $p) {
      if($p['id'] == $idProducto){
            return $p;
    }
 }

 return null;

}


/* Funcion para traer todos los productos */

function catalogo_todas_clase() : array {
$resultado = [];
$catalogo = catalogo_completo();

foreach ($catalogo as $p) {
    $resultado[] = $p;
}

return $resultado;

}

function producto_destacado() : array {
    $resultado = [];
    $catalogo = catalogo_completo();

    foreach ($catalogo as $p) {
        if($p['destacado'] ?? false){
            $resultado[] = $p;
    }
    }

    return $resultado;

}









